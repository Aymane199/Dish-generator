package com.example.mic.projet_v3.DataAdapter;
import android.content.Context;
import android.graphics.Bitmap;
import android.graphics.BitmapFactory;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.ArrayAdapter;
import android.widget.ImageView;
import android.widget.TextView;

import com.example.mic.projet_v3.R;
import com.example.mic.projet_v3.Tables.Ingredient;

import java.util.ArrayList;


public class dataAdapter_ingredient extends ArrayAdapter<Ingredient>{

    Context context;
    ArrayList<Ingredient> mingredient;


    public dataAdapter_ingredient(Context context, ArrayList<Ingredient> ingredient){
        super(context, R.layout.listingredients, ingredient);
        this.context=context;
        this.mingredient=ingredient;
    }

    public  class  Holder{
        TextView nameFV;
        ImageView pic;
    }

    @Override
    public View getView(int position, View convertView, ViewGroup parent) {
        // Get the data item for this position

        Ingredient data = getItem(position);
        // Check if an existing view is being reused, otherwise inflate the view

        Holder viewHolder; // view lookup cache stored in tag

        if (convertView == null) {


            viewHolder = new Holder();
            LayoutInflater inflater = LayoutInflater.from(getContext());
            convertView = inflater.inflate(R.layout.listingredients, parent, false);

            viewHolder.nameFV = (TextView) convertView.findViewById(R.id.txtViewer);
            viewHolder.pic = (ImageView) convertView.findViewById(R.id.imgView);


            convertView.setTag(viewHolder);
        } else {
            viewHolder = (Holder) convertView.getTag();
        }


        viewHolder.nameFV.setText("  "+data.getIngredient_NAME()+"");
        viewHolder.pic.setImageBitmap(convertToBitmap(data.getIngredient_POTO()));


        // Return the completed view to render on screen
        return convertView;
    }
    //get bitmap image from byte array

    private Bitmap convertToBitmap(byte[] b){

        return BitmapFactory.decodeByteArray(b, 0, b.length);

    }

}